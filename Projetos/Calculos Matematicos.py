# Perdir ao usuário que digite dois numeros 
num1 = float(input('Digite o primeiro numero: '))
num2 = float(input('Digite o segundo numero: '))

# Realizar os calculos matematicos 
soma = num1 + num2
subtração = num1 - num2
multiplicação = num1 * num2
divisão = num1 / num2
exponenciação = num1 ** num2

# Prints 
print(f' Soma: {soma}')
print(f' Subtração: {subtração}')
print(f' Multiplicação: {multiplicação}')
print(f' Divisão: {divisão}')
print(f' Exponenciação: {exponenciação}')

